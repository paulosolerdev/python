import math

num = int(input(f"Informe um número: "))

print(f"O dobro de {num} é igual a: {num * 2}")
print(f'O triplo de {num} é igual a: {num * 3}')


# Duas formas de exponenciação em Python (abaixo)
print(f'A raiz quadrada {num} é igual a: {num ** (1/2):.2f}')

print(f'A raiz quadrada {num} é igual a: {pow(num,1/2):.2f}')

